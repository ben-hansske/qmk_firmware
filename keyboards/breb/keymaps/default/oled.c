void render_main(void) {
    oled_set_cursor(0, 0);
    render_wpm();
    oled_set_cursor(0, 4);
    render_qmk_logo();
    oled_set_cursor(0, 10);
    render_keyboard();
    oled_set_cursor(0, 14);
    render_prompt();
}

void oled_task_user(void) {
    if (show_img) {
        render_image();
    } else {
        render_main();
    }
}

