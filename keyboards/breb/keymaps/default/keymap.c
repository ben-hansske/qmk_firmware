bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case BREAD:
            if (record->event.pressed) {
                SEND_STRING("Hello This Is A One Key Keyboard I built");
            }
            break;
    }
    return true;
};

